def add(a, b):
  answer = a + b
  return answer

def subtract(a, b):
  answer = a - b
  return answer

def multiply(a, b):
  answer = a * b
  return answer

def divide(a, b):
  answer = a / b
  return answer

def exp(a, b):
  answer = a ** b
  return answer

print(add(2, 3))
print(subtract(3, 4))
print(multiply(4, 5))
print(divide(5, 6))
print(exp(6, 7))